    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:

        String Idmenu = Tmenu.getText();
        String Idnamamenu = Tnamamenu.getText();
        String harga = Tharga.getText();
        
        if (Idmenu.isEmpty() || Idnamamenu.isEmpty() || harga.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Field ID Menu, Nama Menu, dan Harga harus diisi!");

            return;
        }
        Connection koneksi = Koneksi.getconnection();

        String query = "INSERT INTO menu (Id_menu, Nama_menu, Harga) VALUES (?, ?, ?)";

        try {
            PreparedStatement pst = koneksi.prepareStatement(query);

            pst.setString(1, Idmenu);
            pst.setString(2, Idnamamenu);
            pst.setString(3, harga);

            pst.execute();
            
            AmbilData();
            TampilkanData();
            

        } catch (SQLException ex) {
            Logger.getLogger(EntriMenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_simpanActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String Idmenu = Tmenu.getText();
        String Idnamamenu = Tnamamenu.getText();
        String harga = Tharga.getText();

        Connection db = Koneksi.getconnection();
        String Query = "UPDATE menu SET Nama_menu=?, Harga=? WHERE id_menu=?";
        try {
            PreparedStatement PS = db.prepareStatement(Query);
            PS.setString(1, Idnamamenu);
            PS.setString(2, harga);
            PS.setString(3, Idmenu);
            PS.executeUpdate();
            AmbilData();
            TampilkanData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        Tmenu.setText("");
        Tnamamenu.setText("");
        Tharga.setText("");
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        Connection con = Koneksi.getconnection();
        String query = "DELETE FROM menu WHERE id_menu = ?";
        try {
            PreparedStatement pst = con.prepareStatement(query);
            int selectedRow = Tbarang.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus");
                return;
            }
            String NS = Tbarang.getValueAt(selectedRow, 0).toString();
            pst.setString(1, NS);
            pst.execute();
            con.close();
            JOptionPane.showMessageDialog(null, "Berhasil Delete Data Pada Tabel");
            AmbilData();
            TampilkanData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        Tmenu.setText("");
        Tnamamenu.setText("");
        Tharga.setText("");
    }//GEN-LAST:event_deleteActionPerformed

