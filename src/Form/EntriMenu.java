    private void TbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TbarangMouseClicked
        // TODO add your handling code here:
        int sr = Tbarang.getSelectedRow();
        String NS = Tbarang.getValueAt(sr, 0).toString();
        for (Menu m : menu) {
            if (m.getId_menu() == Integer.parseInt(NS)) {
                Tmenu.setText(String.valueOf(m.getId_menu()));
                Tnamamenu.setText(m.getNama_menu());
                Tharga.setText(String.valueOf(m.getHarga()));
            }
        }
    }//GEN-LAST:event_TbarangMouseClicked
    private void AmbilData() {
        try (Connection DB = Koneksi.getconnection()) {
            PreparedStatement Query = DB.prepareStatement("SELECT * FROM menu");
            ResultSet Hasil = Query.executeQuery();
            menu.clear();
            while (Hasil.next()) {
                menu.add(new Menu(
                        Hasil.getInt("id_menu"),
                        Hasil.getInt("harga"),
                        Hasil.getString("nama_menu")
                ));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void TampilkanData() {
        DefaultTableModel TB = (DefaultTableModel) Tbarang.getModel();
        TB.setRowCount(0);
        for (Menu M : menu) {
            Object[] isi = new Object[4];
            isi[0] = M.getId_menu();
            isi[1] = M.getNama_menu();
            isi[2] = M.getHarga();
            TB.addRow(isi);
        }
        Tbarang.setModel(TB);
    }

