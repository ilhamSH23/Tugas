    private void TablePMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablePMouseClicked
        // TODO add your handling code here:
        int sr = TableP.getSelectedRow();
        String NS = TableP.getValueAt(sr, 0).toString();
        for (Mpelanggan p : Mp) {
            if (p.getId_pelanggan() == Integer.parseInt(NS)) {
                Tpelanggan.setText(String.valueOf(p.getId_pelanggan()));
                Npelanggan.setText(p.getNama_pelanggan());
                No_hp.setText(p.getJenis_kelamin());
                alamat.setText(String.valueOf(p.getNo_hp()));
            }
        }
    }//GEN-LAST:event_TablePMouseClicked
    private void AmbilData() {
        try (Connection DB = Koneksi.getconnection()) {
            PreparedStatement Query = DB.prepareStatement("SELECT * FROM menu");
            ResultSet Hasil = Query.executeQuery();
            Mp.clear();
            while (Hasil.next()) {
                Mp.add(new Mpelanggan(
                        Hasil.getInt("Id_pelanggan"),
                        Hasil.getString("Nama_pelanggan"),
                        Hasil.getString("Jenis_kelamin"),
                        Hasil.getString("No_hp"),
                        Hasil.getString("Alamat")
                ));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void TampilkanData() {
        DefaultTableModel TB = (DefaultTableModel) TableP.getModel();
        TB.setRowCount(0);
        for (Mpelanggan M : Mp) {
            Object[] isi = new Object[4];
            isi[0] = M.getId_pelanggan();
            isi[1] = M.getNama_pelanggan();
            isi[2] = M.getJenis_kelamin();
            isi[3] = M.getNo_hp();
            isi[4] = M.getAlamat();
            TB.addRow(isi);
        }
        TableP.setModel(TB);
    }

