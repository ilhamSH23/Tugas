    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String pelanggan = Tpelanggan.getText();
        String namaP = Npelanggan.getText();
        String Nohp = No_hp.getText();
        String Talamat = alamat.getText();
        String jk = "";
        if (L.isSelected()) {
            jk = "Laki-laki";
        } else {
            jk = "Perempuan";
        }
        if (pelanggan.isEmpty() || namaP.isEmpty() || Nohp.isEmpty() || Talamat.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Field Id Pelanggan, Nama Pelanggan, No Hp dan Alamat harus diisi!");

            return;
        }
        Connection koneksi = Koneksi.getconnection();

        String query = "INSERT INTO pelanggan (Id_pelanggan, Nama_pelanggan, Jenis_kelamin, No_hp, Alamat) VALUES (?, ?, ?, ?, ?, ?)";

        try {
            PreparedStatement pst = koneksi.prepareStatement(query);

            pst.setString(1, pelanggan);
            pst.setString(2, namaP);
            pst.setString(3, Nohp);
            pst.setString(4, Talamat);

            pst.execute();

            AmbilData();
            TampilkanData();

        } catch (SQLException ex) {
            Logger.getLogger(EntriMenu.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_simpanActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        String pelanggan = Tpelanggan.getText();
        String namaP = Npelanggan.getText();
        String Nohp = No_hp.getText();
        String Talamat = alamat.getText();

        Connection db = Koneksi.getconnection();
        String Query = "UPDATE pelanggan SET Nama_pelanggan=?, Jenis_kelamin=?, No_hp=?, Alamat=?, WHERE Id_pelanggan=?";
        try {
            PreparedStatement PS = db.prepareStatement(Query);
            PS.setString(1, pelanggan);
            PS.setString(2, namaP);
            PS.setString(3, Nohp);
            PS.setString(4, Talamat);
            PS.executeUpdate();
            AmbilData();
            TampilkanData();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        Tpelanggan.setText("");
        Npelanggan.setText("");
        No_hp.setText("");
        alamat.setText("");
    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        Connection con = Koneksi.getconnection();
        String query = "DELETE FROM pelanggan WHERE Id_pelanggan = ?";
        try {
            PreparedStatement pst = con.prepareStatement(query);
            int selectedRow = TableP.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(null, "Pilih data yang akan dihapus");
                return;
            }
            String NS = TableP.getValueAt(selectedRow, 0).toString();
            pst.setString(1, NS);
            pst.execute();
            con.close();
            JOptionPane.showMessageDialog(null, "Berhasil Delete Data Pada Tabel");
            AmbilData();
            TampilkanData();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        Tpelanggan.setText("");
        Npelanggan.setText("");
        No_hp.setText("");
        alamat.setText("");
    }//GEN-LAST:event_deleteActionPerformed

